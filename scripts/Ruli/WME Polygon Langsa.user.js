    var kab_mapLayer = new mro_OL.Layer.Vector("Kota Langasa", {		
        displayInLayerSwitcher: true,		
        uniqueName: "__MapkabGroups"		
    });		
        		
    I18n.translations.en.layers.name["__MapkabGroups"] = "Polygon Langsa";		
    mro_Map.addLayer(kab_mapLayer);		
    kab_mapLayer.setVisibility(true);		
		

